package com.javaex.ex01;

public class Ex08 {
	
	public static void main(String[] args) {
		
		//관계 연산자
		int n1=5;
		int n2=3;
		
		System.out.println(n1<n2); // n2가 크니?
		System.out.println(n1>n2); // n1이 크니?
		System.out.println(n1<=n2); //n2가 크거나 n2랑 같니?
		System.out.println(n1>=n2); //n1이 크거나 n2랑 같니?
		System.out.println(n1==n2); //같니?
		System.out.println(n1!=n2); // 다르니?
		// 
		
	}

}
